void ChkLedImgs(WM_HWIN hWin, uint16_t almreg)
{
	int8_t i,img_id;
	static char LED_COM_OK = 0;
	WM_HWIN hItem;
	const void * pData;
  U32          FileSize;
	uint16_t guiobj_id;
	uint16_t guiobj_text;
	uint8_t testbit, shiftqty;
	//#define GUI_EXTRALIGHTGRAY     0x00F0F0F0 //Was 0x00E0E0E0
	uint32_t FaultBkColor = 0;
	uint32_t FaultTextColor = 0;
	
//		{ TEXT_CreateIndirect, "AC"          , ID_TEXT_AC,             XDLGP-10, YPLED-30,  44, 32, 0, 0, 0 },
//  { TEXT_CreateIndirect, "DC"          , ID_TEXT_DC,  (XYDL*1+XDLGP*2)-10, YPLED-30,  44, 32, 0, 0, 0 },
//	{ TEXT_CreateIndirect, "REC"         , ID_TEXT_RCT,(XYDL*2+XDLGP*3)-10, YPLED-30,  44, 32, 0, 0, 0 },
//  { TEXT_CreateIndirect, "SURGE"       , ID_TEXT_SRG,(XYDL*3+XDLGP*4)-20, YPLED-30,  64, 32, 0, 0, 0 },
//  { TEXT_CreateIndirect, "REF"         , ID_TEXT_REF,(XYDL*4+XDLGP*5)-10, YPLED-30,  44, 32, 0, 0, 0 },
//  { TEXT_CreateIndirect, "COM"         , ID_TEXT_COM,(XYDL*5+XDLGP*6)-10, YPLED-30,  44, 32, 0, 0, 0 },
	
	// loop over all 6 leds on the main screen
	for(i=0;i<6;i++)
	{
			switch (i)
			{
				case 0:
					guiobj_id = ID_IMAGE_1; // ACOK LED
				  guiobj_text = ID_TEXT_AC;
					shiftqty = 2;
				break;
				
				case 1:
					guiobj_id = ID_IMAGE_2; // DCOK LED
					guiobj_text = ID_TEXT_DC;
					shiftqty = 1;
				break;
				
				case 2:
					guiobj_id = ID_IMAGE_3; // RECT SUM FLT LED
					guiobj_text = ID_TEXT_RCT;
					shiftqty = 3;
				break;

				case 3:
					guiobj_id = ID_IMAGE_4; // SURGE FLT LED
					guiobj_text = ID_TEXT_SRG;
					shiftqty = 8;
				break;
				
				case 4:
					guiobj_id = ID_IMAGE_5; // REF ELECTRODE FLT LED
					guiobj_text = ID_TEXT_REF;
					shiftqty = 6;
				break;
				
				case 5:
					guiobj_id = ID_IMAGE_6; // COM FLT FLT LED
					guiobj_text = ID_TEXT_COM;
					shiftqty = 7;
				break;
				
				default:
				break;
			}
			
			testbit = almreg>>shiftqty & 0x0001;
			// if bit from alarm reg we are testing this iteration is a 1 illuminate LED if Zero extinguish led
			// by changeing the bmp we assign to the LED locations on the screen
			// EAJ 2/10/2016 - Reverse LED indicator logic: 0 = illuminate LED
			if(!testbit)
			{	
				img_id = ID_IMAGE_0_IMAGE_1;// grn led ON
				//img_id = ID_IMAGE_ON_RED_20x20;// red led ON
				FaultBkColor = GUI_GREEN;
				FaultTextColor = GUI_BLACK;
				if((GuiState.SysDataPSU[GuiState.psu_viewing].psufail_cnt > 1)&&(guiobj_text == ID_TEXT_COM))
				{
					FaultBkColor = GUI_RED;
					FaultTextColor = GUI_WHITE;
				}
				
				if(guiobj_id == ID_IMAGE_6)//COM LED
				{
					if(LED_COM_OK)
					{
						LED_COM_OK=0;
						img_id = ID_IMAGE_0_IMAGE_2;// grn led OFF
						
					}
					else
					{
						LED_COM_OK=1;
						img_id = ID_IMAGE_0_IMAGE_1;// grn led ON
					}
				}
			}
			else
			{
				//img_id = ID_IMAGE_0_IMAGE_2;// grn led OFF
				img_id = ID_IMAGE_ON_RED_20x20;// red led ON
				FaultBkColor = GUI_GRAY;
				FaultTextColor = GUI_BLACK;
			}
			
			if(guiobj_id == ID_IMAGE_2){img_id = ID_IMAGE_0_IMAGE_2;}// grn led OFF}
			if(guiobj_id == ID_IMAGE_4){img_id = ID_IMAGE_0_IMAGE_2;}// grn led OFF}
				
			
			//ID_IMAGE_0_IMAGE_2
			//GuiState.SysDataPSU[psuidx].fail_lockout = 1;
			//GuiState.SysDataPSU[GuiState.psu_viewing].pflparams.psu_mode == P_CPOT
			if((GuiState.SysDataPSU[GuiState.psu_viewing].fail_lockout == 1)&&(guiobj_text == ID_TEXT_COM))
			{
				FaultBkColor = GUI_RED;
				FaultTextColor = GUI_WHITE;
			}
			
			// commit the IMG source change to each LED obj on this screen
			hItem = WM_GetDialogItem(hWin, guiobj_id);// find the LED obj we want to change
			pData = _GetImageById(img_id, &FileSize); // find the bmp img array we want
			IMAGE_SetBMP(hItem, pData, FileSize);     // change the img source for the bmp img obj
			
			if(GuiState.style == 1)
			{
					TEXT_SetBkColor(WM_GetDialogItem(hWin, guiobj_text),FaultBkColor);
					TEXT_SetTextColor(WM_GetDialogItem(hWin, guiobj_text),FaultTextColor);
			}
			else
			{
					TEXT_SetBkColor(WM_GetDialogItem(hWin, guiobj_text),GUI_WHITE);
					TEXT_SetTextColor(WM_GetDialogItem(hWin, guiobj_text),0x00FF0000);
			}
	}
}


